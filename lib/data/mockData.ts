import { useInventoryStore } from '@/lib/stores/inventoryStore';
import { useExpenseStore } from '@/lib/stores/expenseStore';

export function initializeMockData() {
  // Only initialize if stores are empty (client-side only)
  if (typeof window === 'undefined') return;

  // Check if data already exists
  const hasData = useSupplierStore.getState().suppliers.length > 0;

  if (!hasData) {
    useSupplierStore.getState().initializeSuppliers(mockSuppliers);
    useTransactionStore.getState().initializeTransactions(mockTransactions);
    useInvoiceStore.getState().initializeInvoices(mockInvoices);
    useContainerStore.getState().initializeContainers(mockContainers);
    useInventoryStore.getState().initializeItems(mockInventory);
    useExpenseStore.getState().initializeExpenses(mockExpenses);
  }
}

